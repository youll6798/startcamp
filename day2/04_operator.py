# 산술 연산자
print(3 + 5)
print(5 - 2)
print(10 * 10)
print(10 / 2)

print(10 // 3) # 몫
print(10 % 3)  # 나머지

print(2 ** 3) # 2의 3승

# 비교 연산자
print(3 > 5)
print(3 <= 2)
print(3 == 2)
print(3 == '3') # 이건 자료형 자체가 다르기 때문에 False로 나온다
print(3 == int('3')) # 이건 정수형으로 바꿨기 때문에 True로 나온다.
print(3 != 5) # !=는 같지 않다는 말임